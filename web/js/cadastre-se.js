document.getElementById('id-cadastrese').addEventListener('click', function() {    
    // Seleciona os elementos para ocultar
     document.querySelector('.todasSection').style.display = 'none';
       // Exibe o iframe de cadastro
     document.getElementById('id-formCadastro').style.display = 'block';
     document.getElementById('idLiInicio').style.display = 'block';
     
     
    });

    window.scrollTo({
        top: 0,
        behavior: 'smooth' // para uma rolagem suave
    });    
    
    document.getElementById('menu').addEventListener('click', function() {    
        document.querySelector('.todasSection').style.display = 'block';
        // Exibe o iframe de cadastro
        document.getElementById('id-formCadastro').style.display = 'none';
        document.getElementById('idLiInicio').style.display = 'none';
        
});






